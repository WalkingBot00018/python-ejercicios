# ejemplo para saber si el producto de dos numero  enteros   es igual a 100:

n = int(input('dame un numero: '))
m = int(input('dame otro numero: '))

if  n * m == 100:
    print ('el producto (0) * (1) es igual de 100: '.format(n, m))
if n * m != 100:
    print ('el producto (0) * (1) es distinto de 100: '.format(n, m))
    
